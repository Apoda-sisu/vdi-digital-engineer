function mk(source_id, version, clause, content, keywords, skill_tags, category) {
  return {
    clause_id: `${source_id.replace(/[^A-Z0-9]/gi, "")}-${clause.replace(/\./g, "-")}-${SUFFIX}`,
    source_type: "standard",
    source_id,
    version,
    discipline: "water",
    clause,
    content,
    keywords,
    mandatory: true,
    skill_tags,
    category,
  };
}

function pushRows(clauses, source_id, version, rows, category) {
  for (const [clause, content, keywords, tags] of rows) {
    clauses.push(mk(source_id, version, clause, content, keywords, tags, category));
  }
}

